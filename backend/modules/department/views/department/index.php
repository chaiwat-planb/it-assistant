        <?= Html::button('เพิ่มแผนก', ['value' => Url::to('index.php?r=department/department/create'), 'class' => 'btn btn-success', 'id' => 'activity-create-link']) ?>
    </p>

    <?php
    Modal::begin([
        'header' => '<h4>แผนก</h4>',
        'id' => 'activity-modal',
        'size' => 'modal-small',
    ]);
    echo "<div id='modalContent'></div>";
    Modal::end();
    ?>
    <?php $this->registerJs('
        function init_click_handlers(){
            $("#activity-create-link").click(function(e) {
                    $.get(
                        "?r=department/department/create",
                        function (data)
                        {
                            $("#activity-modal").find(".modal-body").html(data);
                            $(".modal-body").html(data);
                            $(".modal-title").html("แผนก");
                            $("#activity-modal").modal("show");
                        }
                    );
                });
                $(".activity-view-link").click(function(e) {
                    var fID = $(this).closest("tr").data("key");
                    $.get(
                        "?r=department/department/view",
                        {
                            id: fID
                        },
                        function (data)
                        {
                            $("#activity-modal").find(".modal-body").html(data);
                            $(".modal-body").html(data);
                            $(".modal-title").html("เปิดดูข้อมูลแผนก");
                            $("#activity-modal").modal("show");
                        }
                    );
                });
            $(".activity-update-link").click(function(e) {
                    var fID = $(this).closest("tr").data("key");
                    $.get(
                        "?r=department/department/update",
                        {
                            id: fID
                        },
                        function (data)
                        {
                            $("#activity-modal").find(".modal-body").html(data);
                            $(".modal-body").html(data);
                            $(".modal-title").html("แก้ไขข้อมูลแผนก");
                            $("#activity-modal").modal("show");
                        }
                    );
                });
            }
        init_click_handlers(); //first run
        $("#customer_pjax_id").on("pjax:success", function() {
          init_click_handlers(); //reactivate links in grid after pjax update
        });'); ?>
    <?php Pjax::begin(); ?>

    <?= GridView::widget([
        'dataProvider' => $dataProvider,
